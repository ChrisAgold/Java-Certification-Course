package java_basics;
// Primitive Data Types
public class VariablesContinued {
    public static void main(String[] args) {
        // int data type
        int num = 100000000;
        //System.out.println(num);
        // Long data type, holds larger numbers
        long bigNumber = 10000000000000L;
        // short data type, can only hold 32767
        short shortNum = 32767;
        // byte, supports 127 maximum
        byte reallySmallNumber = -128;
        // double data type
        double decimalVariable = 394.003;
        // true or false
        boolean decision = false;
        // single character data type
        char letter = 'a';
    }
}